export interface SeedResult {
  [key: string]: { success: boolean; count: number };
}

export async function seedAllData(): Promise<SeedResult> {
  const results: SeedResult = {};

  // Seed Pokemon
  try {
    const { error } = await supabase
      .from("pokemon")
      .upsert(allPokemonData as any, { onConflict: "id" });
    results.pokemon = { success: !error, count: error ? 0 : allPokemonData.length };
  } catch (e) {
    results.pokemon = { success: false, count: 0 };
  }

  // Seed Moves
  try {
    const { error } = await supabase.from("moves").upsert(movesData as any, { onConflict: "id" });
    results.moves = { success: !error, count: error ? 0 : movesData.length };
  } catch (e) {
    results.moves = { success: false, count: 0 };
  }

  // Seed Items
  try {
    const { error } = await supabase.from("items").upsert(itemsData as any, { onConflict: "id" });
    results.items = { success: !error, count: error ? 0 : itemsData.length };
  } catch (e) {
    results.items = { success: false, count: 0 };
  }

  // Seed Locations
  try {
    const { error } = await supabase
      .from("locations")
      .upsert(locationsData as any, { onConflict: "id" });
    results.locations = { success: !error, count: error ? 0 : locationsData.length };
  } catch (e) {
    results.locations = { success: false, count: 0 };
  }

  // Seed Gyms
  try {
    const { error } = await supabase.from("gyms").upsert(gymsData as any, { onConflict: "id" });
    results.gyms = { success: !error, count: error ? 0 : gymsData.length };
  } catch (e) {
    results.gyms = { success: false, count: 0 };
  }

  return results;
}
